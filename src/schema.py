"""
结构化数据模型（Schema）定义

设计思路：
- 基础字段（id, title, source, url, publish_date, language, summary）保存原始信息
- main_topics：新闻的主要话题标签，用于分类聚合和专题分析
- key_points：新闻中的关键事实/事件，用于趋势分析和报告撰写
- impact：新闻对技术/行业/市场的影响力分析，帮助识别潜在风险和机会

为什么这样设计：
- 不是简单做"摘要"，而是设计了多个字段来细致描述新闻的各个层面
- main_topics 支持将新闻归入不同主题类别，便于后续主题聚合分析
- key_points 提取具体的事实要点（技术细节、市场动态、法规变动等）
- impact 提供对行业或市场潜在影响的分析，支撑风险预警和投资机会提示
"""

# 用于 Prompt 中说明 Schema 的简化版本
SCHEMA_PROMPT_DESCRIPTION = """
请对这条新闻进行结构化分析，返回以下 JSON 格式：
{
    "main_topics": ["主要话题标签列表，如 'AI开发工具' '技术发布' 'AI应用' '政策法规' '投融资' '市场趋势' '伦理问题' 等，2-4个"],
    "key_points": [
        "新闻中的关键事实或事件要点1",
        "关键事实或事件要点2",
        "关键事实或事件要点3",
        "..."
    ],
    "impact": "简要分析该新闻对技术、行业或市场的潜在影响，包括可能带来的风险或机会（1-3句话）"
}

注意：
- main_topics 应该是能反映新闻主题方向的标签，帮助后续分类聚合
- key_points 应该是新闻中具体的事实、数据、事件，不是笼统概括，通常3-5个
- impact 应该是从行业视角分析该事件的深远影响，避免空洞描述
"""
